package by.academy.lesson3.classwork;

public class SostovnieOperatori {

	public static void main(String[] args) {
		int sum = 0;
		System.out.println(sum);
		sum = sum + 5;
		System.out.println(sum);
		sum += 1;
		System.out.println(sum);// 6

		sum -= 3;
		System.out.println(sum);// 3

		sum *= 10;
		System.out.println(sum);// 30
	}

}
